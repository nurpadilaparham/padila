package days.of.coding;

public class day93 {
    public static void main(String[] args) {
        //program untuk mencetak huruf a sampai z
        for (char huruf = 'A'; huruf <= 'Z'; huruf++) {//menggunakan perulangan for dimana nama variabelnya adalah huruf dengan nilai awal A
            System.out.print(huruf + " ");//perulangan akan terus berlanjut selama huruf lebih kecul atau sama dengan Z, cetak hurup sampai perulangan selesai
        }
    }
}