package days.of.coding;

public class day51 {
    public static void main(String[] args) {
        // Mendeklarasikan dvariabel boolean dengan nama variabel status
        boolean status = true;

        // Mencetak nilai awal variabel yaitu true
        System.out.println("Nilai awal variabel: " + status);

        // Mengupdate atau mengubah nilai pada variabel menjadi false
        status = false;

        // Mencetak nilai baru yang telah diperbarui
        System.out.println("Nilai setelah pembaruan: " + status);
    }
}
