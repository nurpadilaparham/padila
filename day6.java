package days.of.coding;

public class day6 {
    public static void main(String[] args) {
        //operator aritmatika penjumlahan
        System.out.println("Operator aritmatika");
        int p = 11;
        int a = 29;

        int c = p + a; 
        System.out.println("c = " + c);
        System.out.println("-------------------------------------------------------------------");
        
        // Operator penugasan sederhana
        System.out.println("Operator penugasan");
        int d = 10;

        d = 23; 
        System.out.println("d = " + d);
        System.out.println("-------------------------------------------------------------------");
        
        //Operator Perbandingan lebih besar dari
        System.out.println("Operator perbandingan");
        int i = 13;
        int l = 30;
        
        boolean n = i > l; 
        System.out.println("n = " + n);
        System.out.println("-------------------------------------------------------------------");
        
        //Operator logika AND
        System.out.println("Operator logika");
        boolean q = true;
        boolean s = false;

        boolean t = q && s; 
        System.out.println("t = " + t);
        System.out.println("-------------------------------------------------------------------");

    }
}
