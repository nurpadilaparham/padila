package days.of.coding;

public class day5 {
    public static void main(String[] args) {
        // Deklarasi variabel
        int a = 3;
        int b = 3;

        // Operasi penjumlahan,pengurangan,perkalian,pembagian,sisa bagi
        int c = a + b;
        int d = a - b;
        int e = a * b;
        int f = a / b;
        int g = a % b;
        
        System.out.println("Hasil penjumlahan: " + c);
        System.out.println("Hasil pengurangan: " + d);
        System.out.println("Hasil perkalian: " + e);
        System.out.println("Hasil pembagian: " + f);
        System.out.println("Hasil sisa bagi: " + g);
    }
}

